#include <stdio.h>

/**
 * add- returns the sum of 2 numbers
 * @a: parameter
 * @b: parameter
 * Return: value
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub- returns the difference of 2 numbers
 * @a: parameter
 * @b: parameter
 * Return: value
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul- returns the product of 2 numbers
 * @a: parameter
 * @b: parameter
 * Return: value
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div- returns the dvision of 2 numbers
 * @a: parameter
 * @b: parameter
 * Return: value
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod- returns the modulo of 2 numbers
 * @a: parameter
 * @b: parameter
 * Return: value
 */

int mod(int a, int b)
{
	return (a % b);
}

